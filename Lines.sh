echo "=== ПОДСЧЕТ КОЛИЧЕСТВА СТРОК В ФАЙЛЕ ==="

# Проверка наличия аргумента
if [ $# -eq 0 ]; then
    echo "Ошибка: Укажите имя файла"
    echo "Использование: $0 <имя_файла>"
    exit 1
fi

filename="$1"

# Проверка существования файла
if [ ! -f "$filename" ]; then
    echo "Ошибка: Файл '$filename' не существует или недоступен"
    exit 1
fi

# Подсчет строк
line_count=$(wc -l < "$filename")

echo "Файл: $filename"
echo "Количество строк: $line_count"

